    <h1>Custom Webhooks - Help</h1>
    Having trouble adding webhooks? No problem! Here's some ways you can get those webhooks sorted out. 

    <h2>Currently Supported Actions</h2>
    <p>While all actions can send webhooks, not all will send body data. Here's the list of webhooks that currently are supported:</p>
    <ul>
      <li><b>publish_[post-type]</b>: this one fires when a post is published. Use the post type's slug to determine for what post type the webhook is fired for (i.e. "publish_post" for posts)</li>
    </ul>

    <h2>Contributing</h2>
    <p>If you do come across bugs (or have features you'd like to see added), feel free to report them! If it's a bug, please give as detailed of information to reproduce the bug as you can.</p>
